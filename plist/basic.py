# 1. What is list? How do you create one?
# Ans.
#   A List in python is collection of ordered, mutable, and indexed elements. 
#   It can hold element of any data type including string, integer, and even other lists

my_list = [1, 2, 3, "Python", True]
print(my_list)

# 2. How can you access elements in a list?
# Ans.
#   You can access element by using its index, starting from 0 

print(my_list[0])   # first element
print(my_list[-1])   # last element

